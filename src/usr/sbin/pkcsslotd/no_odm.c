static BOOL BlankPadNoNull ( char *Dest, char *Src, u_int32 Len ) {

  char *s = NULL;

  /* Allocate Len bytes */
  if ( (s = (char *) malloc(Len)) == NULL ) {
    return FALSE;
  }

  /* Fill with blanks */
  memset(s, ' ', Len);

  /* Copy the string, but not the trailing NULL */
  memcpy(s, Src, ( ( strlen(Src) < Len ) ? strlen(Src) : Len ) );

  /* Copy Len bytes into Dest */
  memcpy(Dest, s, Len);

  /* Free storage */
  free(s);

  return TRUE;

}



